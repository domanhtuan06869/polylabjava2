    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        // TODO add your handling code here:
        try {
            String ten = txtTen.getText();
            String gia = txtGia.getText();
            String hang = cbHang.getSelectedItem().toString();
            if (ten.isEmpty() || gia.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Không để trống");
                return;
            }
            if (Double.parseDouble(gia) < 0) {
                JOptionPane.showMessageDialog(null, "Nhập giá lớn hơn 0");
            }
            DongHo dongHo = new DongHo(ten, Double.parseDouble(gia), hang);
            listDongHo.add(dongHo);
            tableModel.addRow(new Object[]{dongHo.getTen(), dongHo.getGia(), dongHo.getHang()});
            ghiFile();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Nhập giá là số");

        }

    }//GEN-LAST:event_btnGhiActionPerformed

