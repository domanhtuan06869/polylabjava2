    public void fillTable() {
        tableModel.setRowCount(0);
        for (DongHo dongHo : listDongHo) {
            Object[] row = new Object[]{dongHo.getTen(), dongHo.getGia(), dongHo.getHang()};
            tableModel.addRow(row);
        }
    }

    public void ghiFile() {
        try {
            FileOutputStream fos = new FileOutputStream("a.txt");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(listDongHo);
            oos.flush();
            fos.flush();
            oos.close();
            fos.close();
        } catch (Exception e) {
            System.out.println("test2.test2.ghiFile()");
        }
    }
